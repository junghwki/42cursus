#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*result;
	size_t	index;

	index = 0;
	if (!(result = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	while (s1[index])
	{
		result[index] = s1[index];
		index++;
	}
	result[index] = '\0';
	return (result);
}
